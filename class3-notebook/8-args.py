# We import the sys module to get access to a few system values
import sys

# sys.argv allows us to have access to parameters passed to a script when its called
argument0 = sys.argv[0]  # Name of script
argument1 = sys.argv[1]  # Provided argument number 1
argument2 = sys.argv[2]  # Provided argument number 2

print(argument0)
print(argument1)
print(argument2)
